export function getAgentDisplayName(agent?: string | null): string {
    switch (agent) {
        case 'claude':
            return 'Claude Code'
        case 'codex':
            return 'Codex'
        case 'gemini':
            return 'Gemini'
        case 'opencode':
            return 'OpenCode'
        case 'cursor':
            return 'Cursor'
        default:
            return agent?.trim() || 'unknown'
    }
}

export function AgentIcon({ agent, className = 'h-4 w-4' }: AgentIconProps) {
    if (agent === 'codex') {
        return (
            <svg className={className} viewBox="0 0 24 24" fill="currentColor" aria-hidden="true">
                <path d={OPENAI_PATH} />
            </svg>
        )
    }

    if (agent === 'claude') {
        return (
            <svg className={className} viewBox="0 0 16 16" fill="currentColor" aria-hidden="true">
                <path d={CLAUDE_PATH} />
            </svg>
        )
    }

    return (
        <span className={`${className} inline-flex items-center justify-center text-[0.85em]`} aria-hidden="true">
            ❖
        </span>
    )
}

export function agentIconColor(agent?: AgentType | string | null): string {
    switch (agent) {
        case 'claude':
            return 'text-[#D97757]'
        case 'codex':
            return 'text-[#10A37F]'
        case 'gemini':
            return 'text-[#4285F4]'
        case 'cursor':
            return 'text-[var(--app-fg)]'
        case 'opencode':
            return 'text-[#F59E0B]'
        default:
            return 'text-[var(--app-hint)]'
    }
}
